class Character:
    '''
    * init function that sets self.symbol to user-provided symbol as a str
    * set_row_coord function that takes row as an int input and sets self.row_coord
    * set_row_coord function that takes row as an int input and sets self.row_coord
    * get_row_coord function returns self.row_coord
    * get_col_coord function returns self.col_coord
    * print_position which prints the position as in autograder output.
    '''
    def __init__(self,symbol: str)-> None:
        self.symbol = symbol
    def set_row_coord(self, row: int)->None:
        self.row_coord = row
    def set_col_coord(self,col: int)->None:
        self.col_coord  = col
    def get_row_coord(self)->int:
        return self.row_coord
    def get_col_coord(self)->int:
        return self.col_coord
    def print_position(self)->None:
        print (self.row_coord,self.col_coord)



